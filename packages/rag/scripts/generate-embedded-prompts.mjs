// Emit TypeScript modules with prompt text inlined. Next.js webpack can resolve
// `import.meta.url` + readFileSync to absolute paths at bundle time, which breaks
// OpenNext/Lambda (`ENOENT` on the developer's machine path). Inlined strings
// survive bundling and need no runtime filesystem for these prompts.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, "..");

/** @param {string} s */
function escapeForTemplateLiteral(s) {
  return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

/** @param {string} relFromPkgRoot */
function readUtf8(relFromPkgRoot) {
  const p = resolve(pkgRoot, relFromPkgRoot);
  return readFileSync(p, "utf8");
}

const system = escapeForTemplateLiteral(readUtf8("src/prompts/system.md"));
const userTpl = escapeForTemplateLiteral(readUtf8("src/prompts/user-template.md"));
const memorySystem = escapeForTemplateLiteral(readUtf8("src/memory/prompt.md"));

const promptsOut = `// Generated by scripts/generate-embedded-prompts.mjs — do not edit by hand.
export const SYSTEM_PROMPT = \`${system}\`;
export const USER_TEMPLATE = \`${userTpl}\`;
`;

const memoryOut = `// Generated by scripts/generate-embedded-prompts.mjs — do not edit by hand.
export const MEMORY_REFRESH_SYSTEM_PROMPT = \`${memorySystem}\`;
`;

writeFileSync(resolve(pkgRoot, "src/prompts/embedded.generated.ts"), promptsOut);
writeFileSync(resolve(pkgRoot, "src/memory/embedded.generated.ts"), memoryOut);
console.log("generate-embedded-prompts: wrote src/prompts/embedded.generated.ts");
console.log("generate-embedded-prompts: wrote src/memory/embedded.generated.ts");
